CREATE DATABASE EMPRESA;
USE EMPRESA;
 
/*CREAR LAS TABLAS*/
CREATE SEQUENCE SUELDOS_seq;
CREATE TABLE SUELDOS(SPID INT DEFAULT NEXTVAL ('SUELDOS_seq') PRIMARY KEY,NPUESTO VARCHAR(50),SALARIO DOUBLE PRECISION, COMISION BOOLEAN NOT NULL);

CREATE SEQUENCE EMPLEADO_seq;
CREATE TABLE EMPLEADO(ID INT DEFAULT NEXTVAL ('EMPLEADO_seq') PRIMARY KEY NOT NULL,NOMBRE VARCHAR(50), APELLIDO VARCHAR(50),TELEFONO VARCHAR(8),ECORREO VARCHAR(255),DIRECCION VARCHAR(255),CIUDAD VARCHAR(50),DEPARTAMENTO VARCHAR(50),PAIS VARCHAR(50),FKPTRAB INT REFERENCES SUELDOS(SPID)); 

CREATE SEQUENCE CLIENTE_seq;
CREATE TABLE CLIENTE(IDC INT DEFAULT NEXTVAL ('CLIENTE_seq') PRIMARY KEY NOT NULL,NOMBRE VARCHAR(50),APELLIDO VARCHAR(50),TELEFONO VARCHAR(8),ECORREO VARCHAR(255));

CREATE SEQUENCE PRODUCTOS_seq;
CREATE TABLE PRODUCTOS(IDP INT DEFAULT NEXTVAL ('PRODUCTOS_seq') PRIMARY KEY NOT NULL,NARTICULO VARCHAR(100),DESCRIPCION VARCHAR(255), TIPO VARCHAR(50), PRECIO DOUBLE PRECISION);

CREATE SEQUENCE ORDEN_seq;
CREATE TABLE ORDEN(OID INT DEFAULT NEXTVAL ('ORDEN_seq') NOT NULL PRIMARY KEY,FKVENDEDOR INT REFERENCES EMPLEADO(ID), FKCLIENTE INT REFERENCES CLIENTE(IDC), FKARTICULO INT REFERENCES PRODUCTOS(IDP),FECHA DATE, CANTIDAD INT, TOTAL FLOAT);
GO
/*FIN CREAR TABLAS*/

/*BORRAR TABLAS*/
DROP TABLE SUELDOS;
DROP TABLE EMPLEADO;
DROP TABLE CLIENTE;
DROP TABLE PRODUCTOS;
DROP TABLE ORDEN;

