CREATE DATABASE EMPRESA;
USE EMPRESA;
 
/*CREAR LAS TABLAS*/
CREATE TABLE SUELDOS(SPID INT AUTO_INCREMENT(99,-1) PRIMARY KEY,NPUESTO VARCHAR(50),SALARIO DOUBLE, COMISION TINYINT NOT NULL);
CREATE TABLE EMPLEADO(ID INT AUTO_INCREMENT(9990,5) PRIMARY KEY NOT NULL,NOMBRE VARCHAR(50), APELLIDO VARCHAR(50),TELEFONO VARCHAR(8),ECORREO VARCHAR(255),DIRECCION VARCHAR(255),CIUDAD VARCHAR(50),DEPARTAMENTO VARCHAR(50),PAIS VARCHAR(50),FKPTRAB INT FOREIGN KEY REFERENCES SUELDOS(SPID);); 
CREATE TABLE CLIENTE(IDC INT AUTO_INCREMENT(1,7) PRIMARY KEY NOT NULL,NOMBRE VARCHAR(50),APELLIDO VARCHAR(50),TELEFONO VARCHAR(8),ECORREO VARCHAR(255));
CREATE TABLE PRODUCTOS(IDP INT AUTO_INCREMENT(9999999,-3) PRIMARY KEY NOT NULL,NARTICULO VARCHAR(100),DESCRIPCION VARCHAR(255), TIPO VARCHAR(50), PRECIO DOUBLE);
CREATE TABLE ORDEN(OID INT AUTO_INCREMENT(100,1) NOT NULL PRIMARY KEY,FKVENDEDOR INT FOREIGN KEY REFERENCES EMPLEADO(ID);, FKCLIENTE INT FOREIGN KEY REFERENCES CLIENTE(IDC), FKARTICULO INT FOREIGN KEY REFERENCES PRODUCTOS(IDP),FECHA DATE, CANTIDAD INT, TOTAL FLOAT);
GO
/*FIN CREAR TABLAS*/

/*BORRAR TABLAS*/
DROP TABLE SUELDOS;
DROP TABLE EMPLEADO;
DROP TABLE CLIENTE;
DROP TABLE PRODUCTOS;
DROP TABLE ORDEN;

