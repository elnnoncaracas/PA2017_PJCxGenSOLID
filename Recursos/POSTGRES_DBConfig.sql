CREATE SEQUENCE SQUERIES_seq;
CREATE TABLE SQUERIES(ID INT DEFAULT NEXTVAL ('SQUERIES_seq') PRIMARY KEY NOT NULL, NCONSULTA VARCHAR(255));

CREATE SEQUENCE TIPOSBD_seq;
CREATE TABLE TIPOSBD(TID INT DEFAULT NEXTVAL ('TIPOSBD_seq') PRIMARY KEY NOT NULL, NOMTIPO VARCHAR(50));

CREATE SEQUENCE PROVEEDORES_seq;
CREATE TABLE PROVEEDORES(ID INT DEFAULT NEXTVAL ('PROVEEDORES_seq') PRIMARY KEY NOT NULL, NOMBREPROV VARCHAR(50), FKTIPO INT REFERENCES TIPOSBD(TID));

CREATE SEQUENCE CADENACONECT_seq;
CREATE TABLE CADENACONECT(CID INT DEFAULT NEXTVAL ('CADENACONECT_seq') PRIMARY KEY NOT NULL, CADENA VARCHAR(255), CADTIP INT REFERENCES PROVEEDORES(ID));

CREATE SEQUENCE IMPOSITORA_seq;
CREATE TABLE IMPOSITORA(IID INT PRIMARY KEY DEFAULT NEXTVAL ('IMPOSITORA_seq') NOT NULL, FKPID INT REFERENCES PROVEEDORES(ID), CCID INT REFERENCES CADENACONECT(CID), TDB INT REFERENCES TIPOSBD(TID));


